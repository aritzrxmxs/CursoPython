# Variables en Python

#Declaración de variables en Python

edad = 28
altura = 1.65
pais = 'España'

# Acceder a las variables
print('Valores Iniciales: ')
print ('Edad:', edad)
print('Altura:', altura)
print('País:', pais)

# Modificar el valor de la variable
print('Valores modificados: ')
edad = 29
altura = 1.81

print ('Edad:', edad)
print('Altura:', altura)
print('País:', pais)

# En Python el tipo es dinámico
edad = 'treinta'
print('Edad:', edad)

# Si queremos acceder a una variable no declarada, hay error.
telefono = '664244538'
print('Telefono:', telefono)