# Operadores aritmeticos

a = 10
b = 3

# Suma
suma = a + b
print(f'Suma: {suma}')

# Resta

resta = a - b
print(f'Resta: {resta}')

# Multiplicacion

multi = a * b
print(f'Multiplicacion: {multi}')

# Division (devuelve flotante)

division = a / b
print(f'División: {division:.2f}')

# División entera

división_entera = a // b
print(f'División entera: {división_entera}')

# Modulo

resto = a % b
print(f'Resto de división: {resto}')

# Exponente (potencia)

potencia = a ** b
print(f'Exponente: {potencia}')