# Largo de una cadena

cadena = 'Hola, mundo!'
largo_cadena = len(cadena)
print(f'Cadena original: {cadena}')
print(f'Largo de la cadena: {largo_cadena}')

# No hace falta meterlo en una variable
cadena2 = 'Hola, soy Aritz'
print(f'\nCadena original: {cadena2}')
print(f'Largo de la cadena: {len(cadena2)}')